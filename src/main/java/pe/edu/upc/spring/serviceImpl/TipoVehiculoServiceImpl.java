package pe.edu.upc.spring.serviceImpl;

import java.util.List;
import java.util.Optional;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import pe.edu.upc.spring.model.TipoVehiculo;
import pe.edu.upc.spring.repository.ITipoVehiculoRepository;
import pe.edu.upc.spring.service.ITipoVehiculoService;

@Service
public class TipoVehiculoServiceImpl implements ITipoVehiculoService {
	@Autowired
	private ITipoVehiculoRepository dTipo;
	
	@Override
	@Transactional(readOnly = true)
	public List<TipoVehiculo> listar() {
		return dTipo.findAll();
	}
	
	@Override
	@Transactional(readOnly = true)
	public Optional<TipoVehiculo> buscarId(int idTipo) {
		return dTipo.findById(idTipo);
	}

	@Override
	public boolean registrar(TipoVehiculo tipo) {
		TipoVehiculo objTipo = dTipo.save(tipo);
		return objTipo !=null;
	}

	@Override
	public void eliminar(int idTipo) {
		dTipo.deleteById(idTipo);
	}
}












































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































